def find_keystream(ct) -> bytes:
    len_ks = max(len(text) for text in ct)
    byte_values = []

    for i in range(len_ks):
        ct_bytes = b''.join(bytes([text[i]]) for text in ct if i < len(text))
        concatenated = len(ct_bytes)

        best_score = float('inf')
        best_byte = None

        for j in range(256):
            guess = xor1(ct_bytes, bytes([j]*concatenated))
            score = score_text(guess)
            if score < best_score:
                best_score = score
                best_byte = j

        assert best_byte is not None
        byte_values.append(best_byte)
    return bytes(byte_values)


